package main

import "fmt"

func main() {
	fmt.Println("One = ", 1)              //int
	fmt.Println("One point five = ", 1.5) //float
	fmt.Println("True = ", true)          //boolean

	fmt.Println("Avinda") //string
	fmt.Println("Avinda Renaldi")
	fmt.Println("Avinda Renaldi Alamsyah")
	fmt.Println(len("Avinda"))

	fmt.Println("Avinda Renaldi"[0]) //binary result
	fmt.Println("Avinda Renaldi Alamsyah"[1])
}
